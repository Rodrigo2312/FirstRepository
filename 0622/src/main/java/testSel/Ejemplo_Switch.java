package testSel;

public class Ejemplo_Switch {

	public static void main(String[] args) {
		int i = 2;
		
		switch (i) {
		case 0:
			System.out.println("I es cero");
			break;
		case 1:
			System.out.println("I es uno");
			break;
		case 2:
			System.out.println("i es dos");
			break;
		default:
			System.out.println("i es mayor que 2");
			break;
		}

	}

}
