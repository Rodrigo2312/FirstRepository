package testSel;

public class Ejemplo_For {

	public static void main(String[] args) {
		int y = 5;
		
		// bucle for comienza cuando x = 0,  X es una variable dentro del bucle for, corre hasta que <=5
		for (int x = 1; x <= y; x++) {
			System.out.println("Valor de x: " + x);
		}

	}

}
